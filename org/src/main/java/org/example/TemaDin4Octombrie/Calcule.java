package org.example.TemaDin4Octombrie;

public class Calcule {
    static void main() {

        final int YEAR_OF_BIRTH = 1991;
        long a = 1234567890;
        long b = 1098765432;
        long sum = a + b;
        long prod = a * b;
        double divizion = a / b;
        long substration = a - b;
        System.out.println(YEAR_OF_BIRTH);
        System.out.println(a + " + " + b + " = " + sum);
        System.out.println(a + " * " + b + " = " + prod);
        System.out.println(a + " / " + b + " = " + divizion);
        System.out.println(a + " - " + b + " = " + substration);


    }
}
